                        <h3>LANGUAGE</h3> 
                        Nothing defines a people more than their language. A language is defined as a system of sounds, written symbols and/or
                        signs used by the people of a particular area or tribe or status to communicate with one another. Language denotes a
                        people’s ability to acquire and use complex systems of communication. Esan language has evolved over the years from the
                        core in Irrua and radiating outwards to distant communities where many dialects exist. In distal communities such as
                        Igueben and Ekpon the Esan language has retained many of the syllables inherited from the Benin Kingdom. Ekpon, the
                        border town of Esan land to the East, often referred to as Delta Igbo, speaks a dialect that blends Esan language and
                        Ika language of Agbor.
                        
                        Igueben language is a very unique one in the sense that it is a combination of Esan language and Bini language
                        
