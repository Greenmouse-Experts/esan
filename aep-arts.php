                    customary law marriage in Nigeria; it is recognized under the Marriage Act. The bride price is usually
                    very low, the
                    impression being that Esan people do not sell their daughters in marriage.
                    The bride price is normally about ₦24 (representing 24 cowries or British pounds used in the
                    pre-colonial and colonial
                    days). A huge sum is usually presented these days, from which the father of the bride or a prominent
                    member of the
                    bride’s family would remove a small amount and refund the balance to the groom for his wife, their
                    daughter`s up
                    keeping. <p></p>
                    Hence the tradition that at death, the corpse of Esan married woman is returned to her family to be
                    buried with her
                    ancestors. <p></p>
                    Prior 1897, girls were generally regarded as ready for marriage between the ages of 15 through 18.
                    Courtship can begin
                    among the individuals during the trip to the river to fetch water or during the moonlight play –
                    EVIONTOI. <p></p>
    
                    Sometimes parents actually go looking for a wife or husband for their children. This led to the
                    BETROTHAL SYSTEM where
                    marriage was conducted with or without the consent of the individuals involved. <br>
                    Sometimes such betrothal, took place
                    when a baby girl was born. Suitors would begin to approach the parents by sending a log of wood or
                    bundle of yam to the
                    parents of the child. <br>
                    You are likely to hear statements such as -” Imu’ Ikerhan gboto”-I have dropped a log of firewood. <br>
                    When a boy decides to get married and the parents have accepted the bride as a prospective
                    daughter-in-law, messages go
                    up and down between the two families. This is called IVBUOMO-SEEKING FOR A BRIDE. <p></p>
                    Series of investigations are conducted by both families – about disease, scandals and crimes which may
                    affect the
                    families. <br>
                    The term of the marriage which of course may include the pride-price would be settled in some families.
                    Gifts
                    for mother of the bride and IROGHAE- members of the extended family would be part of the settlement.
                    Then a date would
                    be fixed for the ceremony which would take place in the home of the would-be bride’s family. <p></p>
    
                    There would of course be a lot of merriment on the day of marriage when the bride and the bridegroom are
                    presented
                    openly to the two families. <p></p>
    
                    Kola nuts and wine are presented. The OKA EGBE of the woman’s family would normally preside over the
                    ceremony. <br>
                    Prayers are said and kola nuts broken at the family shrine. Rituals vary from family to family. <br>
                    The woman always sits on her father’s lap before she is given away. <br>
                    Amidst prayers, laughter and sometimes tears, the woman would be carefully
                    hoisted on the lap of the OKA EGBE of the bride’s family. <p></p>
    
                    Many years ago, the woman would be sent to the bridegroom house about thirteen days after IWANIEN OMO
                    and gingerly
                    hoisted either on her husband’s lap or the OKAEGBE of his family. <br>
                    They are done immediately nowadays in the home of the
                    bridegroom. <br>
                    The bride, now known as OVBIOHA would be led by her relatives to the husband’s house with all her
                    property
                    meanwhile the family and friends of the bridegroom are feasting, drinking, singing and dancing while
                    waiting for the
                    bride to arrive. <p></p>
    
                    The list of items required for Esan traditional marriage vary from one clan to another and one community
                    to another in
                    Esanland. <br>
                    Therefore, it is imperative to contact the would-be in-laws for the marriage list. <br>
