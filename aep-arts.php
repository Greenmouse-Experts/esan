                        <h3>HEALTHCARE</h3> 
                        Healthcare is as old as man. Before the advent of modern medicine, Esan people relied on herbs, bark, and animal parts
                        for treatment of diseases. Beyond this general medicare, Esanland is famous for bone setting.
                        Their medical procedure does not include amputation and use of injections, yet achieve great result and healing,
                        comparable to orthodox method.
                        Patients are brought to Ogwa for bone setting from all over Nigeria because of the successes recorded.
                        Ugbesin is an herbal medicine use for the treatment of accident victims. It dissolves blood clot in the body. Ugbesin is
